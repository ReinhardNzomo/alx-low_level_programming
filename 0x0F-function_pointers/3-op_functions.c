#include "3-calc.h"

/**
* op_add - return sum of two numbers
* @a: first number
* @b: second number
* Return: sum of a and b
*/

int op_add(int a, int b)
{
	return (a + b);
}


/**
* op_sub - return difference of two numbers
* @a: first number
* @b: second number
* Return: diff of a and b
*/

int op_sub(int a, int b)
{
	return (a - b);
}


/**
* op_mul - return multiplication of two numbers
* @a: first number
* @b: second number
* Return: mul of a and b
*/

int op_mul(int a, int b)
{
	return (a * b);
}


/**
* op_div - return division of two numbers
* @a: first number
* @b: second number
* Return: quotient of a and b
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - return modulus of a given b
* @a: first number
* @b: second number
* Return: mod of a divided by b
*/

int op_mod(int a, int b)
{
	return (a % b);
}
